package ex07;

public class Ex02 {
	
	public static void main(String[] args) {
		
		String name = "트롤리";
				
		switch(name)
		{
		case "하린보":
			System.out.println("제 이름은 하린보입니다.");
			break;
		case "하리보":
			System.out.println("제 이름은 하리보입니다.");
			break;
		case "트롤리":
			System.out.println("제 이름은 트롤리입니다.");
			break;
		default:
			System.out.println("같은 이름이 없습니다.");
		}
		
	}

}
