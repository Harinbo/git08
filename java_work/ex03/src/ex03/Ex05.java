package ex03;

public class Ex05 {

	public static void main(String[] args) {
		System.out.print("1n이 없으면 줄바꿈이 안됩니다.");
		System.out.println("=======");
		System.out.println("1n이 있으면 줄바꿈이 됩니다.");
		System.out.print("=======");
		
		System.out.println();
		/*
		 * 아무것도 없이 줄 바꾸라는 뜻
		 */
		
		System.out.println("**************");
		System.out.print("공부한다고\n 다들 고생 \n");
		System.out.print("잘 될꺼에요...\n");
		System.out.println("**************");
	}
	
	// 특수문자 \n and 1n모두 줄바꿈
	
}
